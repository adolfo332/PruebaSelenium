//1) Declara dos variables numéricas (con el valor que desees), muestra por consola la suma, resta, multiplicación, división .
package javabasic.Ejercicios;

public class Ejercicio1 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 2;

		System.out.println("OPERADORES ARIMETICOS");

		System.out.println("Suma de números " + (num1 + num2));
		System.out.println("Resta de números " + (num1 - num2));
		System.out.println("multiplicación de números " + (num1 * num2));
		System.out.println("Divisón de números " + (num1 / num2));
		System.out.println("Modulo de números " + (num1 % num2));
	}

}
